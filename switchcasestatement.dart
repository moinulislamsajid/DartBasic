void main(List<String> args) {
  // switch case
  // switch keyword
  // case:
  // break
  // continue
  // default:
  // switch case only use int and string data type
  // boolean and double can not allowed in dart programming language in switch case
  String grade = '122';
  switch (grade) {
    case 'A':
      print("Very good");
      break;
    case 'B':
      print("Good");
      break;
    case 'C':
      print("Not bed");
      break;
    default:
      print("Not Good");
      break;
  }
}
